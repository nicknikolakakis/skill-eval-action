#!/usr/bin/env python3
"""Post or update a PR comment with eval results.

Uses gh CLI (available on all GitHub-hosted runners) for API calls.
Upserts comments using an HTML marker to avoid duplicates on re-runs.
"""

import json
import os
import subprocess
from pathlib import Path

WORKSPACE = Path(os.environ["WORKSPACE"])
SKILL_NAME = os.environ["SKILL_NAME"]
PASS_THRESHOLD = float(os.environ.get("PASS_THRESHOLD", "80"))
PR_NUMBER = os.environ.get("PR_NUMBER", "")
REPO = os.environ.get("REPO", "")

MARKER = f"<!-- skill-eval: {SKILL_NAME} -->"


def build_comment() -> str:
    """Build the PR comment markdown from summary.json and grading files."""
    summary_path = WORKSPACE / "summary.json"
    if not summary_path.exists():
        return f"{MARKER}\n## Skill Eval: {SKILL_NAME}\n\n⚠️ No results found.\n"

    summary = json.loads(summary_path.read_text())
    pass_rate = summary.get("pass_rate", 0)
    total_passed = summary.get("total_passed", 0)
    total_criteria = summary.get("total_criteria", 0)
    total_time = summary.get("total_time", 0)
    total_tokens = summary.get("total_tokens", 0)
    results = summary.get("results", [])

    threshold_met = pass_rate >= PASS_THRESHOLD
    status = "PASS ✅" if threshold_met else "FAIL ❌"

    lines = [
        MARKER,
        f"## Skill Eval: {SKILL_NAME}",
        "",
        f"**Pass rate: {total_passed}/{total_criteria} ({pass_rate:.1f}%)** | Threshold: {PASS_THRESHOLD:.0f}% | **{status}**",
        "",
        "| # | Case | Status | Criteria | Time | Tokens |",
        "|---|------|--------|----------|------|--------|",
    ]

    for i, r in enumerate(results):
        passed = r.get("criteria_passed", 0)
        total = r.get("criteria_total", 0)
        s = "✅" if passed == total and r.get("status") == "completed" else "❌"
        lines.append(f"| {i+1} | {r['name']} | {s} | {passed}/{total} | {r['elapsed']}s | {r['tokens']:,} |")

    # Collect failed criteria
    failed_details = []
    for r in results:
        case_slug = r["name"].replace(" ", "-").lower()
        grading_path = WORKSPACE / case_slug / "grading.json"
        if not grading_path.exists():
            continue
        grading = json.loads(grading_path.read_text())
        failures = [e for e in grading.get("expectations", []) if not e.get("passed")]
        if failures:
            failed_details.append((r["name"], failures))

    if failed_details:
        lines.append("")
        lines.append("<details>")
        lines.append(f"<summary>Failed criteria ({sum(len(f) for _, f in failed_details)})</summary>")
        lines.append("")
        for case_name, failures in failed_details:
            lines.append(f"**{case_name}**")
            for f in failures:
                evidence = f.get("evidence", "No evidence")[:200]
                lines.append(f"- ❌ {f['text']} — {evidence}")
            lines.append("")
        lines.append("</details>")

    lines.extend([
        "",
        "<details>",
        "<summary>Eval metadata</summary>",
        "",
        f"- **Total time:** {total_time:.1f}s",
        f"- **Total tokens:** {total_tokens:,}",
        f"- **Threshold:** {PASS_THRESHOLD:.0f}%",
        f"- **Timestamp:** {summary.get('timestamp', 'N/A')}",
        "",
        "</details>",
        "",
        "---",
        "*Generated by [skill-eval-action](https://github.com/nicknikolakakis/skill-eval-action)*",
    ])

    return "\n".join(lines)


def find_existing_comment() -> str | None:
    """Find an existing comment with our marker."""
    try:
        result = subprocess.run(
            ["gh", "api", f"repos/{REPO}/issues/{PR_NUMBER}/comments",
             "--jq", f'[.[] | select(.body | contains("{MARKER}"))][0].id'],
            capture_output=True, text=True, timeout=10,
        )
        comment_id = result.stdout.strip()
        return comment_id if comment_id and comment_id != "null" else None
    except Exception:
        return None


def post_or_update_comment(body: str) -> None:
    """Post a new comment or update an existing one."""
    existing_id = find_existing_comment()

    if existing_id:
        subprocess.run(
            ["gh", "api", f"repos/{REPO}/issues/comments/{existing_id}",
             "--method", "PATCH", "--field", f"body={body}"],
            capture_output=True, text=True, timeout=10,
        )
        print(f"Updated existing comment {existing_id}")
    else:
        subprocess.run(
            ["gh", "api", f"repos/{REPO}/issues/{PR_NUMBER}/comments",
             "--method", "POST", "--field", f"body={body}"],
            capture_output=True, text=True, timeout=10,
        )
        print("Posted new comment")


def main() -> None:
    if not PR_NUMBER or not REPO:
        print("No PR context — skipping comment")
        return

    body = build_comment()
    post_or_update_comment(body)


if __name__ == "__main__":
    main()
